package com.JavaTraining.Day2.StringForEachDemo;

public class Cutters {
    public static void main(String[] args) {
        String[] cutters = {
                "nail cutter", "cookie cutter", "cable cutter", "vegetable cutters", "micro cutter"
        };
        for(String cutter:cutters)
            System.out.println(cutter);
    }


}
